
public class TypeConversion {
	public static void main(String []abc) {
		int b=-127;
		byte n;
		n=(byte)b;
	System.out.println(n);  //Explicit type conversion
	
	char ch='a';
	int st;
	st=ch;
	System.out.println(st);//Implicit type conversion OR Automatic type conversion
	/* There is no need for the programmer to forcefully convert the datatype
	 *  
	 */
	
	}
	}